                                <li>Los datos de cualquier persona registrada serán verificados para comprobar que sean reales</li>
                                <h1></h1>
                                <li>Los empleados del sitio web deben mantener una excelente conducta dentro del sitio.</li>
                                <h1></h1>
                                <li>Cualquier oferta de compra o venta por parte de un empleado es realizada bajo su exclusiva responsabilidad y para efectos personales.</li>
                                <h1></h1>
                                <li>Todos los vendedores deben de dar seguimiento al proceso de envió y revisar que este se lleve de manera correcta y adecuada.</li>
                                <h1></h1>
                                <li>Todos los vendedores deberán utilizar un email, teléfono y direcciones para una fácil comunicación cliente-vendedor.</li>
                                <h1></h1>
                                <li>Habrá un límite al uso de información, es decir los vendedores no podrán utilizar la información de contacto de los usuarios para efectos de proponerles transacciones personales con relación a productos publicados por ellos.</li>
                                <h1></h1>
                                <li>El producto ofrecido por el vendedor debe ser exactamente descrito en cuanto estado, condición, tamaño, marca, modelo, color y demás características relevantes.</li>
                                <h1></h1>
                                <li>Los precios de los productos publicados deberán ser expresados con comprobante fiscal incluido cuando corresponda la aplicación del mismo y en moneda del curso legal.</li>
                                <h1></h1>
                                <li>Los precios estipulados en el encabezado del producto deberán ser los mismos a la hora de efectuar el pago de producto.</li>
                                <h1></h1>
                                <li>La descripción del producto publicado se debe ser utilizada para explicar, promover, ofrecer y facilitar la publicación del artículo.</li>
                                <h1></h1>
                                <li>El cliente podrá cancelar su compra siempre y cuando siga en proceso de envió.</li>
                                <h1></h1>
                                <li>El coso de envió dependerá de cada vendedor.</li>
                                <h1></h1>
                                <li>Cada vendedor deberá ser responsable del sustento de sus productos y en dado caso de no tener en existencia, deber de avisar a sus clientes.</li>
                                <h1></h1>
                                <li>El producto debe de ser evaluado dependiendo la calidad de productos, cumplimiento de estándares y reputación.</li>
                                <h1></h1>
